import { type Breakpoint, type SizeScale } from '@veraclins-dev/utils';

type SizeType = 'size' | 'w' | 'h' | 'min-w' | 'min-h' | 'max-w' | 'max-h';

type ExtendedSizeScale =
  | SizeScale
  | 'full'
  | 'auto'
  | 'fit'
  | 'min'
  | 'max'
  | 'dvh'
  | 'dvw'
  | 'lvw'
  | 'lvh'
  | 'svh'
  | 'svw';

type FullSizeScale =
  | ExtendedSizeScale
  | 'screen'
  | '3xs'
  | '2xs'
  | 'xs'
  | 'sm'
  | 'md'
  | 'lg'
  | 'xl'
  | '2xl'
  | '3xl'
  | '4xl'
  | '5xl'
  | '6xl'
  | '7xl';

type SizeValue = Record<ExtendedSizeScale, `${SizeType}-${ExtendedSizeScale}`>;

type FullSizeValue = Record<FullSizeScale, `${SizeType}-${FullSizeScale}`>;

type ResponsiveSizeValue = Record<
  ExtendedSizeScale,
  `${Breakpoint}:${SizeType}-${ExtendedSizeScale}`
>;

type ResponsiveFullSizeValue = Record<
  FullSizeScale,
  `${Breakpoint}:${SizeType}-${FullSizeScale}`
>;

